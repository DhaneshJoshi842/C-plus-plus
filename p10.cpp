#include <iostream>
using namespace std;
int main()
{

    float a = 56.7;
    cout << "value of int(a) is " << int(a) << endl;
    cout << "value of a is " << a << endl;
    int b = 10;
    cout << "sum of a and b is " << a + b << endl;
    cout << "sum of int(a) and b is " << int(a) + b << endl;
}
// this process of converting floating variable into integer variable is called type casting
